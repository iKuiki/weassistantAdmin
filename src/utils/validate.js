/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validAccount(str) {
  // account规则：长度介于4-20之间
  return str.length >= 4 && str.length <= 20
}
